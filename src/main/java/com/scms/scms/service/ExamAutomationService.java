    private List<Student> resolveTeacherStudents(Teacher teacher) {
        if (teacher == null) {
            return List.of();
        }
        ClassRoom classRoom = resolveTeacherClassRoom(teacher);
        if (classRoom != null) {
            return studentRepository.findByClassRoom(classRoom);
        }
        if (teacher.getAdmin() != null) {
            return studentRepository.findByAdmin(teacher.getAdmin());
        }
        return List.of();
    }

    private ClassRoom resolveTeacherClassRoom(Teacher teacher) {
        if (teacher == null) {
            return null;
        }
        if (teacher.getClassRoom() != null && teacher.getClassRoom().getId() != null) {
            return teacher.getClassRoom();
        }
        for (Timetable entry : timetableRepository.findByTeacher(teacher)) {
            if (entry != null && entry.getClassRoom() != null && entry.getClassRoom().getId() != null) {
                return entry.getClassRoom();
            }
        }
        return null;
    }

    private double estimateAttendanceScore(Student student) {
        List<TeacherAttendanceEntry> entries = teacherAttendanceEntryRepository.findByStudentOrderByMarkedAtDesc(student);
        if (entries.isEmpty()) {
            return 68.0;
        }

        long present = entries.stream().filter(entry -> entry.getStatus() != null && entry.getStatus().equalsIgnoreCase("present")).count();
        return Math.max(35.0, (present * 100.0) / entries.size());
    }

    private double estimateExamAbsencePenalty(Student student) {
        List<ExamAttendance> attendances = examAttendanceRepository.findAll().stream()
                .filter(row -> row.getStudent() != null && student.getId() != null && student.getId().equals(row.getStudent().getId()))
                .toList();
        if (attendances.isEmpty()) {
            return 0.0;
        }
        long absent = attendances.stream().filter(row -> row.getStatus() != null && row.getStatus().equalsIgnoreCase("absent")).count();
        return Math.min(absent * 8.0, 24.0);
    }

    private StoredFile storeExamFile(MultipartFile file) {
        String originalName = StringUtils.cleanPath(Objects.requireNonNullElse(file.getOriginalFilename(), ""));
        if (originalName.isBlank()) {
            return null;
        }

        String extension = "";
        if (originalName.contains(".")) {
            extension = originalName.substring(originalName.lastIndexOf('.'));
        }

        if (!isAllowedExamFile(file.getContentType(), extension, originalName)) {
            return null;
        }

        try {
            Path dir = STATIC_ROOT.resolve(EXAM_UPLOAD_DIR);
            Files.createDirectories(dir);
            String storedName = UUID.randomUUID() + extension;
            Path target = dir.resolve(storedName);
            Files.copy(file.getInputStream(), target, java.nio.file.StandardCopyOption.REPLACE_EXISTING);
            return new StoredFile(originalName, storedName, "/uploads/exams/" + storedName, file.getContentType(), file.getSize());
        } catch (IOException e) {
            return null;
        }
    }

    private boolean isAllowedExamFile(String contentType, String extension, String originalName) {
        String normalizedExtension = clean(extension, "").toLowerCase(Locale.ENGLISH);
        String normalizedName = clean(originalName, "").toLowerCase(Locale.ENGLISH);
        Set<String> allowed = Set.of(".pdf", ".doc", ".docx", ".ppt", ".pptx");
        if (allowed.stream().noneMatch(normalizedExtension::endsWith) && !normalizedExtension.isBlank()) {
            return false;
        }
        if (contentType == null || contentType.isBlank()) {
            return true;
        }
        return Set.of(
                "application/pdf",
                "application/msword",
                "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
                "application/vnd.ms-powerpoint",
                "application/vnd.openxmlformats-officedocument.presentationml.presentation"
        ).contains(contentType.toLowerCase(Locale.ENGLISH)) || normalizedName.endsWith(".pdf") || normalizedName.endsWith(".doc")
                || normalizedName.endsWith(".docx") || normalizedName.endsWith(".ppt") || normalizedName.endsWith(".pptx");
    }

    private String seatLabel(int index) {
        int row = ((index - 1) / 4) + 1;
        int seat = ((index - 1) % 4) + 1;
        return "Row " + row + " Seat " + seat;
    }

    private String clean(String value, String fallback) {
        return value == null || value.isBlank() ? fallback : value.trim();
    }

