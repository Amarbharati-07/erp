    @Transactional
    public void saveAttendance(ExamSession examSession, Map<Long, String> statuses) {
        if (examSession == null || statuses == null || statuses.isEmpty()) {
            return;
        }

        LocalDateTime now = LocalDateTime.now();
        for (Map.Entry<Long, String> entry : statuses.entrySet()) {
            Long studentId = entry.getKey();
            String status = clean(entry.getValue(), "PENDING").toUpperCase(Locale.ENGLISH);
            Student student = studentRepository.findById(studentId).orElse(null);
            if (student == null) {
                continue;
            }

            ExamAttendance attendance = examAttendanceRepository.findByExamSessionAndStudent(examSession, student);
            if (attendance == null) {
                attendance = new ExamAttendance();
                attendance.setExamSession(examSession);
                attendance.setStudent(student);
            }
            attendance.setStatus(status);
            attendance.setAutoMarked(false);
            attendance.setMarkedAt(now);
            examAttendanceRepository.save(attendance);
        }
    }

    @Transactional
    public void createPendingExamAttendance(ExamSession examSession) {
        if (examSession == null || examSession.getClassRoom() == null) {
            return;
        }

        List<Student> students = studentRepository.findByClassRoom(examSession.getClassRoom());
        for (Student student : students) {
            if (examAttendanceRepository.findByExamSessionAndStudent(examSession, student) != null) {
                continue;
            }
            ExamAttendance attendance = new ExamAttendance();
            attendance.setExamSession(examSession);
            attendance.setStudent(student);
            attendance.setStatus("PENDING");
            attendance.setAutoMarked(false);
            attendance.setMarkedAt(null);
            examAttendanceRepository.save(attendance);
        }
    }

    @Transactional
    public void finalizeOverdueExams() {
        LocalDateTime now = LocalDateTime.now();
        List<ExamSession> overdue = examSessionRepository
                .findByExamDateTimeBeforeAndStatusNotIn(now, List.of("COMPLETED", "CANCELLED"));

        for (ExamSession exam : overdue) {
            List<ExamAttendance> attendanceRows = examAttendanceRepository.findByExamSessionOrderByStudent_NameAsc(exam);
            for (ExamAttendance row : attendanceRows) {
                if (row.getStatus() == null || row.getStatus().isBlank() || "PENDING".equalsIgnoreCase(row.getStatus())) {
                    row.setStatus("ABSENT");
                    row.setAutoMarked(true);
                    row.setMarkedAt(now);
                }
            }
            exam.setStatus("COMPLETED");
            exam.setFinalizedAt(now);
            exam.setUpdatedAt(now);
            examSessionRepository.save(exam);
            examAttendanceRepository.saveAll(attendanceRows);
        }
    }

