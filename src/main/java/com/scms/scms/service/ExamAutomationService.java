    public boolean isQuizAvailableForStudent(ExamSession examSession, Student student) {
        if (examSession == null || student == null || !examSession.isQuiz()) {
            return false;
        }
        if ("CANCELLED".equalsIgnoreCase(examSession.getStatus())) {
            return false;
        }
        boolean visible = findStudentQuizzes(student).stream()
                .anyMatch(exam -> exam.getId() != null && exam.getId().equals(examSession.getId()));
        if (!visible) {
            return false;
        }
        return examQuizQuestionRepository.countByExamSession(examSession) > 0;
    }

    public LocalDateTime quizEndTime(ExamSession examSession) {
        if (examSession == null || examSession.getExamDateTime() == null) {
            return null;
        }
        return examSession.getExamDateTime().plusMinutes(Optional.ofNullable(examSession.getDurationMinutes()).orElse(20));
    }

    public long quizRemainingSeconds(ExamSession examSession) {
        LocalDateTime end = quizEndTime(examSession);
        if (end == null) {
            return Optional.ofNullable(examSession)
                    .map(ExamSession::getDurationMinutes)
                    .filter(duration -> duration > 0)
                    .orElse(20) * 60L;
        }
        return Math.max(0L, java.time.Duration.between(LocalDateTime.now(), end).getSeconds());
    }

    public long quizRemainingSeconds(ExamSession examSession, ExamQuizAttempt attempt) {
        int durationMinutes = Optional.ofNullable(examSession)
                .map(ExamSession::getDurationMinutes)
                .filter(duration -> duration > 0)
                .orElse(20);
        if (attempt == null || attempt.getStartedAt() == null) {
            return durationMinutes * 60L;
        }
        if (attempt.isSubmitted()) {
            return 0L;
        }
        LocalDateTime end = attempt.getStartedAt().plusMinutes(durationMinutes);
        return Math.max(0L, java.time.Duration.between(LocalDateTime.now(), end).getSeconds());
    }

    private String extractPdfText(MultipartFile file) {
        try (PDDocument document = PDDocument.load(file.getBytes())) {
            PDFTextStripper stripper = new PDFTextStripper();
            String text = stripper.getText(document);
            return text != null ? text.replaceAll("\\s+", " ").trim() : "";
        } catch (IOException ex) {
            return "";
        }
    }

    private List<GeneratedQuestion> generateQuizQuestions(String text, int requestedCount) {
        String normalized = clean(text, "").replace('\u00A0', ' ').trim();
        if (normalized.isBlank()) {
            return List.of();
        }

        List<String> keywordPool = extractKeywordPool(normalized);
        List<String> sentences = Arrays.stream(normalized.split("(?<=[.!?])\\s+"))
                .map(String::trim)
                .filter(sentence -> sentence.length() >= 45 && sentence.length() <= 240)
                .toList();

        List<GeneratedQuestion> generated = new ArrayList<>();
        Set<String> seen = new LinkedHashSet<>();
        Random random = new Random(42L);

        for (String sentence : sentences) {
            if (generated.size() >= requestedCount) {
                break;
            }
            String answer = pickAnswerCandidate(sentence);
            if (answer == null || !seen.add(answer.toLowerCase(Locale.ENGLISH))) {
                continue;
            }
            String maskedSentence = sentence.replaceFirst("(?i)\\b" + Pattern.quote(answer) + "\\b", "_____");
            List<String> distractors = pickDistractors(keywordPool, answer, random);
            if (distractors.size() < 3) {
                continue;
            }
            List<String> options = new ArrayList<>(List.of(answer, distractors.get(0), distractors.get(1), distractors.get(2)));
            Collections.shuffle(options, random);
            String correctOption = optionLabelFor(options.indexOf(answer));
            generated.add(new GeneratedQuestion(
                    "Choose the correct term to complete this statement: " + maskedSentence,
                    options.get(0),
                    options.get(1),
                    options.get(2),
                    options.get(3),
                    correctOption,
                    sentence
            ));
        }

        if (generated.size() >= requestedCount) {
            return generated.subList(0, requestedCount);
        }

        List<String> paragraphs = Arrays.stream(normalized.split("(?<=\\.)\\s+"))
                .filter(value -> value.length() > 80)
                .toList();
        for (String paragraph : paragraphs) {
            if (generated.size() >= requestedCount) {
                break;
            }
            List<String> terms = extractKeywordPool(paragraph);
            if (terms.size() < 4) {
                continue;
            }
            String answer = terms.get(0);
            if (!seen.add(answer.toLowerCase(Locale.ENGLISH))) {
                continue;
            }
            List<String> options = new ArrayList<>(terms.subList(0, 4));
            Collections.shuffle(options, random);
            generated.add(new GeneratedQuestion(
                    "According to the uploaded notes, which concept is most strongly highlighted in this passage?",
                    options.get(0),
                    options.get(1),
                    options.get(2),
                    options.get(3),
                    optionLabelFor(options.indexOf(answer)),
                    paragraph.length() > 220 ? paragraph.substring(0, 220) + "..." : paragraph
            ));
        }
        return generated.subList(0, Math.min(generated.size(), requestedCount));
    }

    private List<String> extractKeywordPool(String text) {
        Set<String> stopWords = new HashSet<>(Arrays.asList(
                "the", "and", "for", "with", "that", "this", "from", "into", "about", "their", "there", "which",
                "when", "where", "while", "these", "those", "have", "will", "been", "being", "were", "what",
                "your", "more", "such", "than", "using", "used", "into", "between", "because", "also", "them",
                "they", "them", "some", "each", "very", "into", "over", "under", "through", "across", "method",
                "system", "process", "example", "chapter", "topic", "notes", "important", "definition"
        ));
        LinkedHashSet<String> keywords = new LinkedHashSet<>();
        for (String token : text.split("[^A-Za-z0-9+#.-]+")) {
            String cleaned = token == null ? "" : token.trim();
            if (cleaned.length() < 5) {
                continue;
            }
            if (!cleaned.matches(".*[A-Za-z].*")) {
                continue;
            }
            String normalized = cleaned.toLowerCase(Locale.ENGLISH);
            if (stopWords.contains(normalized)) {
                continue;
            }
            keywords.add(cleaned);
        }
        return new ArrayList<>(keywords);
    }

    private String pickAnswerCandidate(String sentence) {
        for (String token : sentence.split("[^A-Za-z0-9+#.-]+")) {
            String cleaned = token == null ? "" : token.trim();
            if (cleaned.length() >= 6 && cleaned.matches(".*[A-Za-z].*")) {
                String lower = cleaned.toLowerCase(Locale.ENGLISH);
                if (!Set.of("because", "through", "between", "important", "example", "system", "process").contains(lower)) {
                    return cleaned;
                }
            }
        }
        return null;
    }

    private List<String> pickDistractors(List<String> keywordPool, String answer, Random random) {
        List<String> choices = new ArrayList<>();
        for (String candidate : keywordPool) {
            if (candidate.equalsIgnoreCase(answer)) {
                continue;
            }
            if (Math.abs(candidate.length() - answer.length()) > 8) {
                continue;
            }
            choices.add(candidate);
        }
        Collections.shuffle(choices, random);
        return choices.stream().distinct().limit(3).toList();
    }

    private String optionLabelFor(int index) {
        return switch (index) {
            case 0 -> "A";
            case 1 -> "B";
            case 2 -> "C";
            default -> "D";
        };
    }

    private String normalizeOption(String value) {
        if (value == null || value.isBlank()) {
            return null;
        }
        String normalized = value.trim().toUpperCase(Locale.ENGLISH);
        return Set.of("A", "B", "C", "D").contains(normalized) ? normalized : null;
    }

