    public List<StudentRiskView> buildRiskViews(Teacher teacher) {
        List<Student> students = resolveTeacherStudents(teacher);
        ClassRoom classRoom = resolveTeacherClassRoom(teacher);
        List<Assignment> assignments = classRoom != null
                ? assignmentRepository.findByClassRoomOrderByDueDateAscCreatedAtDesc(classRoom)
                : List.of();
        int openAssignments = (int) assignments.stream().filter(a -> a.isPublished() && (a.getDueDate() == null || !a.getDueDate().isBefore(java.time.LocalDate.now()))).count();
        int materialCount = classRoom != null
                ? studyMaterialRepository.findByClassRoomOrderByUploadedAtDesc(classRoom).size()
                : 0;

        List<StudentRiskView> riskViews = new ArrayList<>();
        for (Student student : students) {
            double attendanceScore = estimateAttendanceScore(student);
            double examPresencePenalty = estimateExamAbsencePenalty(student);
            double classLoadPenalty = Math.min(openAssignments * 3.0, 12.0);
            double materialPenalty = materialCount == 0 ? 6.0 : 0.0;

            double score = 100.0 - (100.0 - attendanceScore) - examPresencePenalty - classLoadPenalty - materialPenalty;
            score = Math.max(0, Math.min(100, score));

            List<String> reasons = new ArrayList<>();
            reasons.add("Attendance score: " + Math.round(attendanceScore) + "%");
            if (examPresencePenalty > 0) {
                reasons.add("Missing exam attendance records: -" + Math.round(examPresencePenalty));
            }
            if (openAssignments > 0) {
                reasons.add("Class assignment load: " + openAssignments + " open");
            }
            if (materialCount == 0) {
                reasons.add("No study materials uploaded for the class yet");
            }

            riskViews.add(new StudentRiskView(
                    student,
                    Math.round(score),
                    score >= 75 ? "Low" : score >= 50 ? "Medium" : "High",
                    String.join("; ", reasons)
            ));
        }

        return riskViews.stream()
                .sorted(Comparator.comparingLong(StudentRiskView::score))
                .toList();
    }

    public List<StudentSeatView> buildSeatingPlan(ExamSession examSession) {
        if (examSession == null || examSession.getClassRoom() == null) {
            return List.of();
        }
        List<Student> students = new ArrayList<>(studentRepository.findByClassRoom(examSession.getClassRoom()));
        students.sort(Comparator.comparing(student -> clean(student.getRollNo(), student.getName()), String.CASE_INSENSITIVE_ORDER));
        List<StudentSeatView> seats = new ArrayList<>();
        for (int i = 0; i < students.size(); i++) {
            Student student = students.get(i);
            seats.add(new StudentSeatView(i + 1, student, clean(student.getRollNo(), "N/A"), seatLabel(i + 1)));
        }
        return seats;
    }

