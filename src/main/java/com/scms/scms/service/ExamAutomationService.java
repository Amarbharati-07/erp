    public List<ExamSession> findStudentExams(Student student) {
        if (student == null) {
            return List.of();
        }

        ClassRoom classRoom = student.getClassRoom();
        LinkedHashMap<Long, ExamSession> visible = new LinkedHashMap<>();
        List<ExamSession> exams = classRoom != null
                ? examSessionRepository.findByClassRoomOrderByExamDateTimeAsc(classRoom)
                : student.getAdmin() != null ? examSessionRepository.findByAdminOrderByExamDateTimeDesc(student.getAdmin()) : List.of();
        for (ExamSession exam : exams) {
            if (exam != null && exam.getId() != null) {
                visible.putIfAbsent(exam.getId(), exam);
            }
        }
        if (student.getAdmin() != null) {
            for (ExamSession exam : examSessionRepository.findByAdminOrderByExamDateTimeDesc(student.getAdmin())) {
                if (exam == null || exam.getId() == null || exam.getTeacher() == null) {
                    continue;
                }
                ClassRoom teacherClassRoom = resolveTeacherClassRoom(exam.getTeacher());
                if (teacherClassRoom != null && classRoom != null
                        && Objects.equals(teacherClassRoom.getId(), classRoom.getId())) {
                    visible.putIfAbsent(exam.getId(), exam);
                }
            }
        }
        return visible.values().stream()
                .filter(exam -> exam.getStatus() == null || !"CANCELLED".equalsIgnoreCase(exam.getStatus()))
                .sorted(Comparator.comparing(ExamSession::getExamDateTime, Comparator.nullsLast(Comparator.naturalOrder())))
                .toList();
    }

