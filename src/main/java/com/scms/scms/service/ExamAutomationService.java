    @Transactional
    public ExamSession createExam(Teacher teacher,
                                  String title,
                                  String subject,
                                  String syllabusTopics,
                                  String instructions,
                                  String seatingInfo,
                                  LocalDateTime examDateTime,
                                  Integer durationMinutes,
                                  String room,
                                  MultipartFile syllabusFile) {
        ClassRoom resolvedClassRoom = resolveTeacherClassRoom(teacher);
        ExamSession exam = new ExamSession();
        exam.setTeacher(teacher);
        exam.setClassRoom(resolvedClassRoom);
        exam.setAdmin(teacher != null ? teacher.getAdmin() : null);
        exam.setTitle(clean(title, "Untitled exam"));
        exam.setSubject(clean(subject, clean(teacher != null ? teacher.getSubject() : null, "Subject")));
        exam.setSyllabusTopics(clean(syllabusTopics, "Topics will be shared soon"));
        exam.setInstructions(clean(instructions, "Follow the teacher instructions."));
        exam.setSeatingInfo(clean(seatingInfo, "Seating will be generated by the system."));
        exam.setExamDateTime(examDateTime);
        exam.setDurationMinutes(durationMinutes != null && durationMinutes > 0 ? durationMinutes : 60);
        exam.setRoom(clean(room, resolvedClassRoom != null ? resolvedClassRoom.getRoom() : "TBD"));
        exam.setStatus("SCHEDULED");
        exam.setExamMode("STANDARD");
        exam.setQuizQuestionCount(0);
        exam.setQuizAutoGenerated(Boolean.FALSE);
        exam.setCreatedAt(LocalDateTime.now());
        exam.setUpdatedAt(LocalDateTime.now());

        if (syllabusFile != null && !syllabusFile.isEmpty()) {
            StoredFile storedFile = storeExamFile(syllabusFile);
            if (storedFile != null) {
                exam.setSyllabusOriginalFileName(storedFile.originalName());
                exam.setSyllabusStoredFileName(storedFile.storedName());
                exam.setSyllabusFilePath(storedFile.webPath());
                exam.setSyllabusContentType(storedFile.contentType());
                exam.setSyllabusFileSize(storedFile.size());
            }
        }

        exam = examSessionRepository.save(exam);
        createExamNotifications(exam, teacher);
        createPendingExamAttendance(exam);
        return exam;
    }

    @Transactional
    public ExamSession createQuizExam(Teacher teacher,
                                      String title,
                                      String subject,
                                      String instructions,
                                      LocalDateTime examDateTime,
                                      Integer durationMinutes,
                                      MultipartFile pdfFile,
                                      Integer questionCount) {
        if (teacher == null || pdfFile == null || pdfFile.isEmpty()) {
            return null;
        }
        String fileName = Objects.requireNonNullElse(pdfFile.getOriginalFilename(), "").toLowerCase(Locale.ENGLISH);
        if (!fileName.endsWith(".pdf")) {
            return null;
        }

        ClassRoom resolvedClassRoom = resolveTeacherClassRoom(teacher);
        String extractedText = extractPdfText(pdfFile);
        List<GeneratedQuestion> generatedQuestions = generateQuizQuestions(
                extractedText,
                Math.max(3, Math.min(questionCount != null ? questionCount : 10, 25))
        );
        if (generatedQuestions.size() < 3) {
            return null;
        }

        ExamSession exam = new ExamSession();
        exam.setTeacher(teacher);
        exam.setClassRoom(resolvedClassRoom);
        exam.setAdmin(teacher.getAdmin());
        exam.setTitle(clean(title, "Online Quiz"));
        exam.setSubject(clean(subject, clean(teacher.getSubject(), "Subject")));
        exam.setSyllabusTopics("Auto-generated MCQ quiz from uploaded PDF notes.");
        exam.setInstructions(clean(instructions, "Read every MCQ carefully. The quiz auto-submits when the timer ends."));
        exam.setSeatingInfo("Online quiz");
        exam.setExamDateTime(examDateTime);
        exam.setDurationMinutes(durationMinutes != null && durationMinutes > 0 ? durationMinutes : 20);
        exam.setRoom("Online Quiz");
        exam.setStatus("SCHEDULED");
        exam.setExamMode("QUIZ");
        exam.setQuizQuestionCount(generatedQuestions.size());
        exam.setQuizAutoGenerated(Boolean.TRUE);
        exam.setCreatedAt(LocalDateTime.now());
        exam.setUpdatedAt(LocalDateTime.now());

        StoredFile storedFile = storeExamFile(pdfFile);
        if (storedFile == null) {
            return null;
        }
        exam.setSyllabusOriginalFileName(storedFile.originalName());
        exam.setSyllabusStoredFileName(storedFile.storedName());
        exam.setSyllabusFilePath(storedFile.webPath());
        exam.setSyllabusContentType(storedFile.contentType());
        exam.setSyllabusFileSize(storedFile.size());

        exam = examSessionRepository.save(exam);
        int order = 1;
        for (GeneratedQuestion generatedQuestion : generatedQuestions) {
            ExamQuizQuestion question = new ExamQuizQuestion();
            question.setExamSession(exam);
            question.setQuestionOrder(order++);
            question.setQuestionText(generatedQuestion.questionText());
            question.setOptionA(generatedQuestion.optionA());
            question.setOptionB(generatedQuestion.optionB());
            question.setOptionC(generatedQuestion.optionC());
            question.setOptionD(generatedQuestion.optionD());
            question.setCorrectOption(generatedQuestion.correctOption());
            question.setExplanation(generatedQuestion.explanation());
            examQuizQuestionRepository.save(question);
        }

        createExamNotifications(exam, teacher);
        return exam;
    }

    public List<ExamSession> findTeacherExams(Teacher teacher) {
        if (teacher == null) {
            return List.of();
        }

        List<ExamSession> exams = new ArrayList<>();
        ClassRoom classRoom = resolveTeacherClassRoom(teacher);
        if (classRoom != null) {
            exams.addAll(examSessionRepository.findByClassRoomOrderByExamDateTimeAsc(classRoom));
        }
        if (exams.isEmpty() && teacher.getAdmin() != null) {
            exams.addAll(examSessionRepository.findByAdminOrderByExamDateTimeDesc(teacher.getAdmin()));
        }
        if (exams.isEmpty()) {
            exams.addAll(examSessionRepository.findByTeacherOrderByExamDateTimeDesc(teacher));
        }
        return exams.stream()
                .filter(exam -> exam.getTeacher() == null || teacher.getId() == null || teacher.getId().equals(exam.getTeacher().getId()))
                .distinct()
                .sorted(Comparator.comparing(ExamSession::getExamDateTime, Comparator.nullsLast(Comparator.naturalOrder())).reversed())
                .toList();
    }

