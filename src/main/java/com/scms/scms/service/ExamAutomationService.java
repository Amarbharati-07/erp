    @Transactional
    public ExamQuizAttempt startQuizAttempt(ExamSession examSession, Student student) {
        if (examSession == null || student == null || !examSession.isQuiz() || !isQuizAvailableForStudent(examSession, student)) {
            return null;
        }
        ExamQuizAttempt existing = findQuizAttempt(examSession, student);
        if (existing != null) {
            if (existing.isSubmitted()) {
                return existing;
            }
            if (existing.getStartedAt() == null) {
                existing.setStartedAt(LocalDateTime.now());
                existing.setStatus("IN_PROGRESS");
                return examQuizAttemptRepository.save(existing);
            }
            return existing;
        }

        ExamQuizAttempt attempt = new ExamQuizAttempt();
        attempt.setExamSession(examSession);
        attempt.setStudent(student);
        attempt.setAdmin(student.getAdmin());
        attempt.setStartedAt(LocalDateTime.now());
        attempt.setStatus("IN_PROGRESS");
        attempt.setTotalQuestions((int) examQuizQuestionRepository.countByExamSession(examSession));
        attempt.setScore(0);
        attempt.setCorrectAnswers(0);
        attempt.setAutoSubmitted(Boolean.FALSE);
        return examQuizAttemptRepository.save(attempt);
    }

    @Transactional
    public ExamQuizAttempt submitQuizAttempt(ExamSession examSession,
                                             Student student,
                                             Map<Long, String> answers,
                                             boolean autoSubmitted) {
        if (examSession == null || student == null || !examSession.isQuiz()) {
            return null;
        }
        ExamQuizAttempt attempt = findQuizAttempt(examSession, student);
        if (attempt == null) {
            attempt = startQuizAttempt(examSession, student);
        }
        if (attempt == null || attempt.isSubmitted()) {
            return attempt;
        }

        List<ExamQuizQuestion> questions = findQuizQuestions(examSession);
        int correctAnswers = 0;
        for (ExamQuizQuestion question : questions) {
            String selectedOption = normalizeOption(answers.get(question.getId()));
            boolean correct = selectedOption != null && selectedOption.equalsIgnoreCase(question.getCorrectOption());
            if (correct) {
                correctAnswers++;
            }
            ExamQuizResponse response = examQuizResponseRepository.findByAttemptAndQuestion(attempt, question)
                    .orElseGet(ExamQuizResponse::new);
            response.setAttempt(attempt);
            response.setQuestion(question);
            response.setSelectedOption(selectedOption);
            response.setCorrectOption(question.getCorrectOption());
            response.setCorrect(correct);
            examQuizResponseRepository.save(response);
        }

        attempt.setCorrectAnswers(correctAnswers);
        attempt.setTotalQuestions(questions.size());
        attempt.setScore(correctAnswers);
        attempt.setSubmittedAt(LocalDateTime.now());
        attempt.setStatus("SUBMITTED");
        attempt.setAutoSubmitted(autoSubmitted);
        return examQuizAttemptRepository.save(attempt);
    }

